/**
 * Dropdown translations.
 */
export interface DropdownTranslations {
  /**
   * Select placeholder.
   * @type {string}
   */
  selectPlaceholder?: string;

  /**
   * Search placeholder.
   * @type {string}
   */
  searchPlaceholder?: string;

  /**
   * Search empty result text.
   * @type {string}
   */
  searchEmptyResult?: string;

  /**
   * Select all checkbox placeholder.
   * @type {string}
   */
  selectAllPlaceholder?: string;

  /**
   * Selected items wrap placeholder.
   * Used when item count > wrapDisplaySelectLimit
   * @type {string}
   */
  selectedItemWrapPlaceholder?: string;
}
